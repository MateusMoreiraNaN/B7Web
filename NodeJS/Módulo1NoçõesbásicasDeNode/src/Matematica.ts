function soma(x: number, y: number): number{
    return x + y
}

function subtrair(x: number, y: number): number{
    return x - y
}

function multiplicar(x: number, y: number): number{
    return x + y
}

export default{
    soma,
    subtrair,
    multiplicar,
}

